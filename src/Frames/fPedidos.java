    private void jbot_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbot_guardarActionPerformed

        if (jtb_id.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un ID");
            jtb_id.requestFocus();
            return;
        }

        if (jtb_nombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un nombre al pedido");
            jtb_nombre.requestFocus();
            return;
        }

        if (jtb_idClient.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un ID de Cliente");
            jtb_idClient.requestFocus();
            return;
        }
        
        if (jtb_cliente.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el nombre del cliente");
            jtb_cliente.requestFocus();
            return;
        }

        if (jtb_IDRepart.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un ID de repartidor");
            jtb_IDRepart.requestFocus();
            return;
        }

        if (jtb_repart.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar el nombre del repartidor");
            jtb_repart.requestFocus();
            return;
        }

        if (jtb_fecha.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una fecha");
            jtb_fecha.requestFocus();
            return;
        }

        if (jtb_azuc.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una cantidad de azucar");
            jtb_azuc.requestFocus();
            return;
        }
        
        if (jtb_int.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una cantidad de integral");
            jtb_int.requestFocus();
            return;
        }
        
        if (jtb_fran.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una cantidad de frances");
            jtb_fran.requestFocus();
            return;
        }
        
        if (jtb_total.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una cantidad total");
            jtb_total.requestFocus();
            return;
        }

        dPedidos dts = new dPedidos();
        lPedidos func = new lPedidos();

        dts.setId_pedido(Integer.parseInt(jtb_id.getText()));
        dts.setNombre(jtb_nombre.getText());
        dts.setNumClient(Integer.parseInt(jtb_idClient.getText()));
        dts.setCliente(jtb_cliente.getText());
        dts.setNumRepart(Integer.parseInt(jtb_IDRepart.getText()));
        dts.setRepartidor(jtb_repart.getText());
        dts.setFecha(jtb_fecha.getText());
        dts.setCant_azuc(Integer.parseInt(jtb_azuc.getText()));
        dts.setCant_integ(Integer.parseInt(jtb_int.getText()));
        dts.setCant_fran(Integer.parseInt(jtb_fran.getText()));
        dts.setCant_total(Integer.parseInt(jtb_total.getText()));

        if (accion.equals("Guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El Pedido fue registrado satisfactoriamente");
                mostrar("");
                habilitar();
            }

        } else if (accion.equals("Actualizar")) {
            dts.setId_pedido(Integer.parseInt(jtb_id.getText()));

            if (func.actualizar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El Pedido fue actualizado satisfactoriamente");
                mostrar("");
                habilitar();
            }
        }
        jbot_guardar.setText("Guardar");
        accion = "Guardar";
    }//GEN-LAST:event_jbot_guardarActionPerformed

