    private void jbot_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbot_guardarActionPerformed

        if (jtb_nombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un nombre");
            jtb_nombre.requestFocus();
            return;
        }

        if (jtb_direccion.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una direccion");
            jtb_direccion.requestFocus();
            return;
        }

        if (jtb_telefono.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un telefono");
            jtb_telefono.requestFocus();
            return;
        }

        if (jtb_edad.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una edad");
            jtb_edad.requestFocus();
            return;
        }

        if (jtb_tipo.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un tipo");
            jtb_tipo.requestFocus();
            return;
        }

        dRepartidores dts = new dRepartidores();
        lRepartidor func = new lRepartidor();

        dts.setNombre(jtb_nombre.getText());
        dts.setDireccion(jtb_direccion.getText());
        dts.setTelefono(jtb_telefono.getText());
        dts.setEdad(jtb_edad.getText());
        dts.setTipo(Integer.parseInt(jtb_tipo.getText()));

        if (accion.equals("Guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El Repartidor fue registrado satisfactoriamente");
                mostrar("");
                habilitar();
            }

        } else if (accion.equals("Actualizar")) {
            dts.setId_repart(Integer.parseInt(jtb_id.getText()));

            if (func.actualizar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El Repartidor fue actualizado satisfactoriamente");
                mostrar("");
                habilitar();
            }
        }
        jbot_guardar.setText("Guardar");
        accion = "Guardar";
    }//GEN-LAST:event_jbot_guardarActionPerformed

