    private void jbot_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbot_guardarActionPerformed

        if (jtb_cantidad.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar ID");
            jtb_id.requestFocus();
            return;
        }

        if (jtb_nombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar un nombre");
            jtb_nombre.requestFocus();
            return;
        }

        if (jtb_clase.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una clase");
            jtb_clase.requestFocus();
            return;
        }

        if (jtb_cantidad.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes ingresar una cantidad");
            jtb_cantidad.requestFocus();
            return;
        }

        dPan dts = new dPan();
        lPan func = new lPan();

        dts.setId_pan(Integer.parseInt(jtb_id.getText()));
        dts.setNombre(jtb_nombre.getText());
        dts.setClase(jtb_clase.getText());
        dts.setCantidad(Integer.parseInt(jtb_cantidad.getText()));

        if (accion.equals("Guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El Pan fue registrado satisfactoriamente");
                mostrar("");
                habilitar();
            }

        } else if (accion.equals("Actualizar")) {
            dts.setId_pan(Integer.parseInt(jtb_id.getText()));

            if (func.actualizar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El Pan fue actualizado satisfactoriamente");
                mostrar("");
                habilitar();
            }
        }
        jbot_guardar.setText("Guardar");
        accion = "Guardar";
    }//GEN-LAST:event_jbot_guardarActionPerformed

